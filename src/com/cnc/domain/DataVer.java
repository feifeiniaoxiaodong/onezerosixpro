package com.cnc.domain;

/**
 * 标准版本信息
 * @author wei
 *
 */
public class DataVer {
	private String NCK_VER;
	private String DRV_VER;
	private String PLC_VER;
	private String CNC_VER;
	private String NC_VER;
		
	public String getNCK_VER() {
		return NCK_VER;
	}
	public void setNCK_VER(String nCK_VER) {
		NCK_VER = nCK_VER;
	}
	public String getDRV_VER() {
		return DRV_VER;
	}
	public void setDRV_VER(String dRV_VER) {
		DRV_VER = dRV_VER;
	}
	public String getPLC_VER() {
		return PLC_VER;
	}
	public void setPLC_VER(String pLC_VER) {
		PLC_VER = pLC_VER;
	}
	public String getCNC_VER() {
		return CNC_VER;
	}
	public void setCNC_VER(String cNC_VER) {
		CNC_VER = cNC_VER;
	}
	public String getNC_VER() {
		return NC_VER;
	}
	public void setNC_VER(String nC_VER) {
		NC_VER = nC_VER;
	}

}
